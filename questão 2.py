#imprimir a soma, diferença, produto, e divisão

a = float(input("digite o primeiro numero"))
b = float(input("digite o segundo numero"))

soma = a + b
diferenca = a - b
produto = a * b
divisao = a / b

print(f"A soma dos dois números é {soma} \n"
      f"A diferença dos dois números é {diferenca} \n"
      f"O produto dos dois números é {produto} \n" 
      f"A divisão entre os dois números é {divisao} \n" )